function create() {

    fx = pacman.add.audio('sfx');

    pacman.stage.backgroundColor = '#000000';
    map = pacman.add.tilemap('fondo');

    //  The first parameter is the tileset name, as specified in the Tiled map editor (and in the tilemap json file)
    //  The second parameter maps this name to the Phaser.Cache key 'tiles'
    map.addTilesetImage('pacManTiles', 'tiles');
    map.setCollisionBetween(2,401);
    map.setCollisionBetween(403,420);
    
    //  Creates a layer from the World1 layer in the map data.
    //  A Layer is effectively like a Phaser.Sprite, so is added to the display list.
    layer = map.createLayer('pacman');

    //  This resizes the game world to match the layer dimensions
    layer.resizeWorld();
    pacman.physics.startSystem(Phaser.Physics.ARCADE);

    //grup de punts
    dot = pacman.add.group();
    var i = 0;
    for (var y=0; y < (33*16); y+=16)
    {
        for (var x=0; x < (30*16); x+=16){
            if(dots[i]==1){
                dot.create(x, y, 'dot_image');
            }
            i++;
        }
        
    }
    pacman.physics.arcade.enable(dot);

    //grup de punts_importants
    dot_important = pacman.add.group();
    var i = 0;
    for (var y=0; y < (33*16); y+=16)
    {
        for (var x=0; x < (30*16); x+=16){
            if(dots_important[i]==1){
                dot_important.create(x, y, 'dot_image2');
            }
            i++;
        }
        
    }
    pacman.physics.arcade.enable(dot_important);

    //grup de canvis de direcció cantonades
    direction = pacman.add.group();
    var i = 0;
    for (var y=0; y < (33*16); y+=16)
    {
        for (var x=0; x < (30*16); x+=16){
            if(directions[i]==1){
                direction.create(x, y, 'dot_invisible');
            }
            i++;
        }
        
    }
    pacman.physics.arcade.enable(direction);

    //grup de canvis de direcció x
    directionx = pacman.add.group();
    var i = 0;
    for (var y=0; y < (33*16); y+=16)
    {
        for (var x=0; x < (30*16); x+=16){
            if(directionsx[i]==1){
                directionx.create(x, y, 'dot_invisible');
            }
            i++;
        }
        
    }
    pacman.physics.arcade.enable(directionx);

    //grup de canvis de direcció y
    directiony = pacman.add.group();
    var i = 0;
    for (var y=0; y < (33*16); y+=16)
    {
        for (var x=0; x < (30*16); x+=16){
            if(directionsy[i]==1){
                directiony.create(x, y, 'dot_invisible');
            }
            i++;
        }
        
    }
    pacman.physics.arcade.enable(directiony);

    iniciaposicions();

    upKey = pacman.input.keyboard.addKey(Phaser.Keyboard.UP);
    downKey = pacman.input.keyboard.addKey(Phaser.Keyboard.DOWN);
    leftKey = pacman.input.keyboard.addKey(Phaser.Keyboard.LEFT);
    rightKey = pacman.input.keyboard.addKey(Phaser.Keyboard.RIGHT);
}

